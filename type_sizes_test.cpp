#include <iostream>

using namespace std;

int main(){
    cout << "Size of int: "          << sizeof(int)          << " bytes\n";
    cout << "Size of unsigned int: " << sizeof(unsigned int) << " bytes\n";
    cout << "Size of char: "         << sizeof(char)         << " bytes\n";
    cout << "Size of double: "       << sizeof(double)       << " bytes\n";
    return 0;
}